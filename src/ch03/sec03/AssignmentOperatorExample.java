package ch03.sec03;

public class AssignmentOperatorExample {
    public static void main(String[] args) {

        int resul = 0;
        resul += 10;
        System.out.println("resul = " + resul);
        resul -= 5;
        System.out.println("resul = " + resul);
        resul *= 3;
        System.out.println("resul = " + resul);
        resul /= 5;
        System.out.println("resul = " + resul);
        resul %= 3;
        System.out.println("resul = " + resul);
    }
}
