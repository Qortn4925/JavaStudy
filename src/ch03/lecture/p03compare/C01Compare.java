package ch03.lecture.p03compare;

public class C01Compare {
    // 비교연산자 
    //  같다(==) , 같지 않다(!=),
    //  크다(>) , 크거나 같다(>=),
    // 작다 (<), 작거나 같다(<=)
    //연산 결과는 boolean  >false true,
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);

    }

}
